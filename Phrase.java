public class Phrase{
    public static void main(String[] args){
        System.out.println("English                       |    Portuguese ");
        System.out.println("Good morning                  |    bom Dia        ");
        System.out.println("It is a pleasure to meet you. |    É um prazer te conhecer");
        System.out.println("Please call me tomorrow.      |    por favor meligue amanhã.");
        System.out.println("Have a nice day!              |    Tenha um bom dia!        ");
    }
}